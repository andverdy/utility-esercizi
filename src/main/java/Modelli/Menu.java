package Modelli;

import java.util.List;

public class Menu {

	private List<String> antipasti;
	private List<String> primi;
	private List<String> secondi;
	private List<String> frutta;

	public List<String> getAntipasti() {
		return antipasti;
	}

	public void setAntipasti(List<String> antipasti) {
		this.antipasti = antipasti;
	}

	public List<String> getPrimi() {
		return primi;
	}

	public void setPrimi(List<String> primi) {
		this.primi = primi;
	}

	public List<String> getSecondi() {
		return secondi;
	}

	public void setSecondi(List<String> secondi) {
		this.secondi = secondi;
	}

	public List<String> getFrutta() {
		return frutta;
	}

	public void setFrutta(List<String> frutta) {
		this.frutta = frutta;
	}

}
